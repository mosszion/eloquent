function repeatLog(n) {
    for ( let i= 0; i <n ; i++ ) {
        console.log(i);
    }
}

repeatLog(5);


//////////////////////////////////////////////
//////////////////////// Abstraction 2
//////////////////////////////////////////////

function repeat(n, callback) {
    for (let i =0; i< n; i++) {
        callback(i)
    }
}

repeat(5, console.log)

//////////////////////////////////////////////
//////////////////////// Abstraction 3
//////////////////////////////////////////////

let lables = [];
